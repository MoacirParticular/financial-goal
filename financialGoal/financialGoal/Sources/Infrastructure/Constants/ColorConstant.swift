import UIKit

public extension UIColor {
    @objc
    static var backgroundColor: UIColor {
        return UIColor.white
    }
    
    @objc
    static var textColor: UIColor {
        return UIColor.black
    }
    
    @objc
    static var labelColor: UIColor {
        return UIColor.gray
    }
}

